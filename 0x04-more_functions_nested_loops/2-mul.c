/*
 * mul -  Multiplies two integers 'a' and 'b'.
 *
 * mul function takes two integers, 'a' and 'b', and returns their product.
 *
 * param 'a' The first integer.
 * param 'b' The second integer.
 *
 * return The product of 'a' and 'b'.
 */
int mul(int a, int b)
{
	return (a * b);
}

